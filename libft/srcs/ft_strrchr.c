#include "../includes/libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = ft_strlen(s);
	if (c == '\0')
		return ((char *)s + i);
	while (0 < i--)
	{
		if ((char)s[i] == (char)c)
			return ((char *)s + i);
	}
	return (0);
}
