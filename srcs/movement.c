static void move(int direction, t_data *info)
{
	if (direction == 1)
	{
		if (!mov_map[(int)(info->posX + info->dirX * info->moveSpeed)][(int)(info->posY)])
			info->posX += info->dirX * info->moveSpeed;
		if (!mov_map[(int)(info->posX)][(int)(info->posY + info->dirY * info->moveSpeed)])
			info->posY += info->dirY * info->moveSpeed;
	}
	else if (direction == -1)
	{
		if (!mov_map[(int)(info->posX - info->dirX * info->moveSpeed)][(int)(info->posY)])
			info->posX -= info->dirX * info->moveSpeed;
		if (!mov_map[(int)(info->posX)][(int)(info->posY - info->dirY * info->moveSpeed)])
			info->posY -= info->dirY * info->moveSpeed;
	}
}

static void rotate(int direction, t_data *info)
{
	double oldDirX;
	double oldPlaneX;
	oldDirX = info->dirX;
	oldPlaneX = info->planeX;

	if (direction == 1)
	{
		info->dirX = info->dirX * cos(-info->rotSpeed) - info->dirY * sin(-info->rotSpeed);
		info->dirY = oldDirX * sin(-info->rotSpeed) + info->dirY * cos(-info->rotSpeed);
		info->planeX = info->planeX * cos(-info->rotSpeed) - info->planeY * sin(-info->rotSpeed);
		info->planeY = oldPlaneX * sin(-info->rotSpeed) + info->planeY * cos(-info->rotSpeed);
	}
	else if (direction == -1)
	{
		info->dirX = info->dirX * cos(info->rotSpeed) - info->dirY * sin(info->rotSpeed);
		info->dirY = oldDirX * sin(info->rotSpeed) + info->dirY * cos(info->rotSpeed);
		info->planeX = info->planeX * cos(info->rotSpeed) - info->planeY * sin(info->rotSpeed);
		info->planeY = oldPlaneX * sin(info->rotSpeed) + info->planeY * cos(info->rotSpeed);
	}
}

int key_press(int key, t_data *info)
{
	if (key == W_KEY)
		move(1, info);
	if (key == S_KEY)
		move(-1, info);
	if (key == D_KEY)
		rotate(1, info);
	if (key == A_KEY)
		rotate(-1, info);
	if (key == ESC_KEY)
		exit(0);
	return (0);
}
